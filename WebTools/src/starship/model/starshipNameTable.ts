const chooseOptions = (era: Era, type: RandomStarshipCharacterType, spaceframeType?: CharacterType, spaceframe?: Spaceframe) => {
    if (type === RandomStarshipCharacterType.Klingon || spaceframeType === CharacterType.KlingonWarrior) {
        let result = [...klingonEnterpriseEraNames];
        switch (era) {
            case Era.NextGeneration:
                Array.prototype.push.apply(result, klingonTngEraNames);
                break;
            default:
        }
        return result;
    } else if (type === RandomStarshipCharacterType.Romulan) {
        let result = [...starshipRomulanNames];
        return result;
    } else if (type === RandomStarshipCharacterType.Civilian) {
        let result = [...starshipCivilianNames];
        return result;
    } else {
        let result = [...starshipEnterpriseEraNames];
        switch (era) {
            case Era.OriginalSeries:
                Array.prototype.push.apply(result, starshipTosEraName);
                break;
            case Era.NextGeneration:
                Array.prototype.push.apply(result, starshipTosEraName);
                Array.prototype.push.apply(result, starshipTngEraNames);
                break;
            default:
        }

        if (spaceframe != null && spaceframeNames[spaceframe] != null) {
            const names = spaceframeNames[spaceframe];
            result.push(...names)
        }
        return result;
    }
}

export const StarshipRandomNameTable = (era: Era, type: RandomStarshipCharacterType, spaceframeType?: CharacterType, spaceframe?: Spaceframe) => {

    let options = chooseOptions(era, type, spaceframeType, spaceframe);
    let index = Math.floor(Math.random() * options.length);

    return options[index];
}